    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre,apellido,dni;
        if(!txtNombre.getText().trim().equals("")){
            nombre = txtNombre.getText();
            if(!txtApellido.getText().trim().equals("")){
                apellido = txtApellido.getText();
                if (txtDni.getText().length()==8){
                    dni = txtDni.getText();
                    if (jrbEsporadico.isSelected()){
                        c.setApellido(apellido);
                        c.setNombre(nombre);
                        c.setDni(dni);
                        //c = new Cliente(nombre, apellido, dni);
                        c.setEsHabitual(false);
                        c.setDescuento(0.0);
                        JOptionPane.showMessageDialog(this, "Datos del cliente: \n- Nombre: "+c.getNombre()+"\n- Apellido: "+c.getApellido()+"\n- Tipo de cliente: Esporadico\n- Descuento: "+c.getDescuento(), "Cliente registrado con Exito", 1);
                        Clientes.insertar(c);
                        limpiarCajas();
                        dispose();
                        
                    }else{
                        if (jrbtHabitual.isSelected()){
                            //c = new Cliente(nombre, apellido, dni);
                            c.setApellido(apellido);
                            c.setNombre(nombre);
                            c.setDni(dni);
                            c.setEsHabitual(true); 
                            JOptionPane.showMessageDialog(this, "Datos del cliente: \n- Nombre: "+c.getNombre()+"\n- Apellido: "+c.getApellido()+"\n- Tipo de cliente: Habitual\n- Descuento: "+c.getDescuento(), "Cliente registrado con Exito", 1);
                            Clientes.insertar(c);
                            limpiarCajas();
                            dispose();
                            
                        }else{
                            JOptionPane.showMessageDialog(this, "Seleccione que tipo de cliente es por favor", "Error", 0);
                        }
                    }

                }else{
                    JOptionPane.showMessageDialog(this, "Ingrese un numero con 8 digitos por favor", "Error", 0);
                    txtDni.setText(null);
                    txtDni.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Ingrese un apellido por favor", "Error", 0);
                txtApellido.requestFocus();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese un nombre por favor", "Error", 0);
            txtNombre.requestFocus();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

